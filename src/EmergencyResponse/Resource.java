package EmergencyResponse;

public class Resource {
	int p;
	int f;
	int a;
	public Resource(int p, int f, int a) {
		super();
		this.p = p;
		this.f = f;
		this.a = a;
	}
	public int getP() {
		return p;
	}
	public void setP(int p) {
		this.p = p;
	}
	public int getF() {
		return f;
	}
	public void setF(int f) {
		this.f = f;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	
	
	
}
